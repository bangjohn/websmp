<h3>KEGIATAN PRAMUKA PENGGALANG</H3>
<P>Kegiatan dalam tingkatan penggalang antara lain:<BR>
1. Jambore: adalah pertemuan pramuka penggalang dalam bentuk perkemahan besar, dan diadakan bertingkat; Jambore Nasional(Jamnas), Jambore Daerah (Jamda), Jambore Cabang, Jambore Ranting<BR>
2. Lomba Tingkat, adalah pertemuan regu-regu Pramuka Penggalang dalam bentuk lomba kegiatan kepramukaan. Lomba tingkat dilaksanakan secara berjenjang dimulai dari tingkat gugusdepan (LT-I), ranting (LT-II), cabang (LT-III), daerah (LT-IV), nasional (LT-V).<BR>
3. Penjelajahan (Wide Game), adalah pertemuan Pramuka Penggalang dalam bentuk mencari jejak (orienteenering) dengan menggunakan tanda-tanda jejak, membuat peta, mencatat berbagai situasi dan dibagi dalam pos-pos. Setiap pos berisi kegiatan keterampilan kepramukaan seperti morse/semaphore, sandi, tali temali dan sejenisnya. Dalam membuat peta, pramuka penggalang memiliki teknik tersendiri seperti peta pita dan peta lapangan. Peta pita dibuat oleh dua atau tiga orang yang biasanya mencatat posisi atau titik dari kompas bidik, kemudian orang yang lain akan mencatat kondisi sekitar dalam sebuah meja jalan. Meja lanan sendiri berbentuk papan seukuran kertas folio yang kemudian ditempel kertas yang digulung panjang<BR>
4. Perkemahan, adalah pertemuan Pramuka Penggalang yang dilaksanakan secara reguler, untuk mengevaluasi hasil latihan di gugusdepan. Perkemahan diselenggarakan dalam bentuk Persami (Perkemahan Sabtu Minggu), Perjusami (Perkemahan Jumat Sabtu Minggu), perkemahan liburan dan sejenisnya.<BR>
5. Karnaval, adalah kegiatan pawai yang menampilkan hasil kreatifitas Pramuka Penggalang.<BR>
